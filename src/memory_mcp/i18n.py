def t(key: str, lang: str = DEFAULT_LANG, **kwargs: Any) -> str:
    """Translate *key* into *lang*, formatting with **kwargs if provided.

    Falls back to English if the key or language is missing.
    """
    lang = _normalize_lang(lang)
    table = TRANSLATIONS.get(key, {})
    msg = table[lang] if lang in table else table.get(DEFAULT_LANG, key)
    if kwargs:
        try:
            msg = msg.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return msg


def detect_lang(request: Any) -> str:  # request: starlette.requests.Request
    """Detect preferred language from HTTP Accept-Language header.

    Returns a 2-char code like 'en', 'ko', 'ja', etc.
    Falls back to DEFAULT_LANG if nothing matches.
    """
    try:
        header = request.headers.get("accept-language", "")
        return _parse_accept_language(header)
    except Exception:
        return DEFAULT_LANG


def detect_lang_from_code(code: str) -> str:
    """Normalize a raw locale code (e.g. 'ko_KR', 'ja', 'zh-CN') to a supported lang.

    Returns DEFAULT_LANG if not supported.
    """
    if not code:
        return DEFAULT_LANG
    return _normalize_lang(code[:2].lower())


# ─────────────────────────────────────────────────────────────────────────────
# Internals
# ─────────────────────────────────────────────────────────────────────────────

_LANG_ALIASES: dict[str, str] = {
    "zh": "en",  # Chinese → English (not yet supported)
    "ru": "en",
    "ar": "en",
    "hi": "en",
}


def _normalize_lang(code: str) -> str:
    code = code.lower()[:2]
    if code in SUPPORTED_LANGS:
        return code
    return _LANG_ALIASES.get(code, DEFAULT_LANG)


def _parse_accept_language(header: str) -> str:
    """Parse Accept-Language header, return best matching supported language."""
    if not header:
        return DEFAULT_LANG
    # e.g. "ko-KR,ko;q=0.9,en-US;q=0.8,en;q=0.7"
    candidates: list[tuple[float, str]] = []
    for part in header.split(","):
        part = part.strip()
        if not part:
            continue
        if ";q=" in part:
            lang_part, q_part = part.split(";q=", 1)
            try:
                q = float(q_part)
            except ValueError:
                q = 1.0
        else:
            lang_part = part
            q = 1.0
        code = lang_part.strip().split("-")[0].lower()
        candidates.append((q, code))
    candidates.sort(key=lambda x: -x[0])
    for _, code in candidates:
        normalized = _normalize_lang(code)
        if normalized != DEFAULT_LANG or code == DEFAULT_LANG or code == "en":
            return normalized
    return DEFAULT_LANG


